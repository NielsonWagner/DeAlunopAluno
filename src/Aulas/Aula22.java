package Aulas;

public class Aula22 {
    
    public static void main (String [] args){
        
        //Utilizando o comando "While"
        int i = 8;
        while (i < 20){
           System.out.println(i);
           ++i;
       }
        
        
        //Utilizando o comando "Do While"
        int a = 1;
        do {
            System.out.println(a);
            ++a;
        } while (a < 10);
    
        
        
        
        
    
}
    
}